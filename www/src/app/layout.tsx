import type { Metadata } from 'next';
import Script from 'next/script';
import { Geist, Geist_Mono } from 'next/font/google';
import './globals.scss';
import '@fontsource/dosis';
import '@fontsource/cinzel';
import '@fontsource/vibur';
import { LanguageProvider } from '../contexts/language';
import { ClientNavigationProvider } from '@/providers/client-navigation.provider';
import StickyHeaderInit from '@/components/sticky-header-init';
import { LoadingProvider } from '@/providers/loading';
import { DataProvider } from '@/providers/data';
import { CurrentRecipeProvider } from '@/providers/current-recipe';
import { NavigationProvider } from '@/providers/navigation.provider';
import { cookies } from 'next/headers';

const geistSans = Geist({
  variable: '--font-geist-sans',
  subsets: ['latin'],
});

const geistMono = Geist_Mono({
  variable: '--font-geist-mono',
  subsets: ['latin'],
});

export const metadata: Metadata = {
  title: 'Create Next App',
  description: 'Generated by create next app',
  // App icons and manifest
  icons: {
    icon: '/logo.png',
    apple: '/logo.png',
    shortcut: '/logo.png',
  },
  manifest: '/manifest.json',
};

export default async function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  // Resolve theme directly from cookie set by backend (no extra SSR fetch)
  let themeAttr: string | undefined;
  try {
    const jar: any = (cookies as any)();
    const store = jar && typeof jar.then === 'function' ? await jar : jar;
    const val = store?.get?.('theme')?.value as string | undefined;
    if (val && typeof val === 'string' && val.trim()) themeAttr = val.trim();
  } catch { /* ignore */ }
  return (
    <html lang="pt" data-theme={themeAttr}>
      <body
        suppressHydrationWarning
        className={`${geistSans.variable} ${geistMono.variable}`}
      >
        {/* Google Identity Services for Sign-In */}
        <Script
          src="https://accounts.google.com/gsi/client"
          strategy="afterInteractive"
          async
        />
        {/* Initialize Ionicons custom element on the client */}{/* Initialize page-wide sticky header behavior on client */}
        <StickyHeaderInit />
        <NavigationProvider>
          <LoadingProvider>
            <DataProvider>
              <CurrentRecipeProvider>
                <LanguageProvider>{children}</LanguageProvider>
              </CurrentRecipeProvider>
            </DataProvider>
          </LoadingProvider>
        </NavigationProvider>
      </body>
    </html>
  );
}


